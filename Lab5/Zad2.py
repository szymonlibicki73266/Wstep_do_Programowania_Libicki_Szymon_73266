# a) pierwiastek z 81
# b) 8 do potęgi 10
# c) pierwiastek z 2 + pierwiastek z 3 + pierwiastek z 6
# d) pierwiastek z -5
# e) pierwiastek 3 stopnia z 125
import math

print(f"a) pierwiastek z 81: {math.sqrt(81)}")
print(f"b) 8 do potęgi 10: {8**10}")
print(f"c) pierwiastek z 2 + pierwiastek z 3 + pierwiastek z 6: {math.sqrt(2) + math.sqrt(3) + math.sqrt(6)}")
print(f"d) pierwiastek z -5: {math.sqrt(-5)}")
print(f"e) pierwiastek 3 stopnia z 125: {math.pow(125, 1/3)}")